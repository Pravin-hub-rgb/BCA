mylist = [556, "Mothi", 84, 96, 84, 75, 84]

#Access 0th element
print(mylist[0])

#Access 0th to first element
print(mylist[0:2])

#Access 2nd to end of the list
print(mylist[1:])

#Accessing starting to ending element
print(mylist[:])

#Access last index value
print(mylist[-1])

#Access element in reverse order
print(mylist[::-1])